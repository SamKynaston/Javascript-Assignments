//Do..While Loop
do {
    i = i + 1;
    console.log(i)
} while (i < 5)

/*
This statement executes the loop and then checks the condition. If it evaluates to false, it stops running.
*/

//While Loop
let x = 0
while (x < 5) {
    x = x + 1;
    console.log(x)
}

/*
This is different as it checks the condition before it executes the loop.
*/

/*
These two loops are pretty much the same. Howevber, a do while loop has to execute code; a while loop does not. This simply means that if you want a guarantee that code will run, a do...while loop is the best.
*/